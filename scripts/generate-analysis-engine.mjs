#!/usr/bin/env node
/**
 * Bundle the canonical Kernel analysis engine for zero-build CLI consumers.
 * The committed output is a distribution artifact; this script is its only writer.
 */
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { build } from 'tsup';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const target = path.join(root, 'bin/lib/analysis-engine.mjs');
const check = process.argv.includes('--check');
const temporary = fs.mkdtempSync(path.join(os.tmpdir(), 'arkgate-analysis-engine-'));

try {
  await build({
    entry: { analysis: path.join(root, 'src/kernel/analysisBundle.ts') },
    format: ['esm'],
    outDir: temporary,
    bundle: true,
    splitting: false,
    sourcemap: false,
    dts: false,
    clean: true,
    // This committed file ships beside the built public API. Keep the canonical
    // TypeScript readable while avoiding a second verbose copy in the npm tarball.
    minify: true,
    treeshake: false,
    silent: true,
    target: 'es2022',
  });

  const built = fs.readFileSync(path.join(temporary, 'analysis.js'), 'utf8');
  const output =
    '// GENERATED from src/kernel/analysis.ts by scripts/generate-analysis-engine.mjs.\n' +
    '// Do not edit this file directly.\n' +
    built;
  if (check) {
    if (!fs.existsSync(target) || fs.readFileSync(target, 'utf8') !== output) {
      console.error(
        'bin/lib/analysis-engine.mjs is out of date. Run npm run generate:analysis-engine.'
      );
      process.exitCode = 1;
    } else {
      console.log('✔ bin/lib/analysis-engine.mjs is up to date.');
    }
  } else {
    fs.writeFileSync(target, output);
    console.log('✔ generated bin/lib/analysis-engine.mjs.');
  }
} finally {
  fs.rmSync(temporary, { recursive: true, force: true });
}
